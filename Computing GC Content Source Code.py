def parse_fasta(data):
    fasta_dict = {}
    label = None
    for line in data.strip().splitlines():
        if line.startswith('>'):
            label = line[1:].strip()
            fasta_dict[label] = ""
        else:
            fasta_dict[label] += line.strip()
    return fasta_dict

def gc_content(sequence):
    gc_count = sum(1 for base in sequence if base in 'GC')
    return (gc_count / len(sequence)) * 100

def highest_gc_content(fasta_data):
    fasta_dict = parse_fasta(fasta_data)
    max_gc_id = None
    max_gc_content = 0
    for label, sequence in fasta_dict.items():
        gc = gc_content(sequence)
        if gc > max_gc_content:
            max_gc_content = gc
            max_gc_id = label
    return max_gc_id, max_gc_content

