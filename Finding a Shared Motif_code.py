def longest_common_substring(dna_strings):
    # Get the first DNA string from the collection
    first_string = dna_strings[0]
    
    # Function to check if a substring exists in all DNA strings
    def is_common_substring(substring):
        for dna in dna_strings:
            if substring not in dna:
                return False
        return True
    
    # Variable to store the longest common substring
    longest_substring = ""
    
    # Length of the first DNA string
    len_first = len(first_string)
    
    # Check all substrings of the first string starting from the largest possible size
    for length in range(len_first, 0, -1):
        for start in range(len_first - length + 1):
            # Extract substring
            substring = first_string[start:start + length]
            
            # Check if the substring is common to all DNA strings
            if is_common_substring(substring):
                # If this is the longest found so far, update the result
                if len(substring) > len(longest_substring):
                    longest_substring = substring
    
    return longest_substring

def parse_fasta(input_data):
    # Parse the input data in FASTA format
    sequences = []
    current_sequence = ""
    
    for line in input_data.splitlines():
        if line.startswith(">"):  # It's a header line
            if current_sequence:
                sequences.append(current_sequence)
            current_sequence = ""
        else:
            current_sequence += line.strip()
    
    # Add the last sequence after the loop
    if current_sequence:
        sequences.append(current_sequence)
    
    return sequences

